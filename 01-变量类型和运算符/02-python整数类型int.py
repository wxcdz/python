# 十六进制
hex1 = 0x45
hex2 = 0x4AF

print("hex1Value:", hex1)
# hex1Value: 69
print("h3x2Value:", hex2)
# h3x2Value: 1199


# 二进制
bin1 = 0b10
print("bin1Value:", bin1)
# Output: bin1Value: 2
bin2 = 0b110
print("bin2Value:", bin2)
# Output: bin2Value: 6

#八进制
oct1 = 0o26
print("oct1Value:", oct1)
# Output: oct1Value: 22
oct2 = 0O41
print("oct2Value:", oct2)
# Output: oct2Value: 33

